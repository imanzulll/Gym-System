    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            dbCon.dbConnector();
            String status = "";
            String sql = "SELECT status FROM `Payment` WHERE member_id ='" + mem_id.getText() + "' and package_id= '" + pid.getText() + "'";
            ResultSet rss = dbCon.st.executeQuery(sql);
            if (rss.next()) {
                status = rss.getString("status");
            }
            if (status.equals("Not Paid")) {
                String sql1 = "UPDATE Payment SET status ='Paid' WHERE member_id ='" + mem_id.getText() + "' and package_id = '" + pid.getText() + "'";
                dbCon.st.executeUpdate(sql1);
                JOptionPane.showMessageDialog(null, "Information Has Been Saved Successfully", "INFORMATION!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "This Member Id has already paid !!!", "WARNING !!", JOptionPane.WARNING_MESSAGE);
            }
            dbCon.dbClose();
            rss.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

