    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (((mem_id.getText()).equals(""))) {
                JOptionPane.showMessageDialog(null, "Details are Missing \nPlease Enter the Member ID!", "Warning!!!", JOptionPane.WARNING_MESSAGE);
            } else if (pid.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Details are Missing \nPlease Enter the Package Id", " WARNING....", JOptionPane.WARNING_MESSAGE);
            } else {
                dbCon.dbConnector();
                String coach_id = "";
                String sql = "SELECT * FROM `Payment` WHERE member_id='" + mem_id.getText() + "' and package_id='" + pid.getText() + "'";
                ResultSet rss = dbCon.st.executeQuery(sql);
                if (rss.next()) {
                    String add1 = rss.getString("payment_date");
                    pdate.setText(add1);
                    String add2 = rss.getString("package_amount");
                    pamount.setText(add2);
                    String add3 = rss.getString("package_info");
                    pinfo.setText(add3);
                } else {
                    JOptionPane.showMessageDialog(null, "Not found anything for this UserName\nplease login again");
                }
                rss.close();
                dbCon.dbClose();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

